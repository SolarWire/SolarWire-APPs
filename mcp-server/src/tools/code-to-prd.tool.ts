export function registerCodeToPrdTool(server: McpServer) {
  server.tool(
    "code-to-prd",
    "Reverse engineer frontend/backend code into complete PRD documents with SolarWire wireframes. Scans codebase structure, extracts UI components, API endpoints, and generates documentation.",
    {
      codebasePath: z.string().describe("Path to the codebase directory"),
      frontendPath: z.string().optional().default("src").describe("Frontend source directory"),
      backendPath: z.string().optional().default("api").describe("Backend source directory"),
      projectName: z.string().describe("Project name for output directory"),
      outputDir: z.string().optional().default("./output").describe("Output directory (default: .solarwire)"),
    },
    async ({ codebasePath, frontendPath, backendPath, projectName, outputDir }) => {
      try {
        logger.info(`Starting code-to-prd analysis for: ${projectName}`);

        const basePath = path.resolve(codebasePath);
        if (!fs.existsSync(basePath)) {
          return {
            content: [{ type: "text", text: `Error: Codebase path not found: ${basePath}` }],
            isError: true,
          };
        }

        // Scan project structure
        const structure = scanProject(basePath, [frontendPath], [backendPath]);

        logger.info(`Scan complete: ${structure.frontendFiles.length} frontend, ${structure.backendFiles.length} backend files`);

        // Generate PRD content
        const prdContent = generatePrdContent(projectName, structure);

        // Save PRD file
        const outDir = path.resolve(outputDir);
        if (!fs.existsSync(outDir)) {
          fs.mkdirSync(outDir, { recursive: true });
        }

        const prdFile = path.join(outDir, `${projectName.toLowerCase().replace(/\s+/g, "-")}-prd.md`);
        fs.writeFileSync(prdFile, prdContent, "utf-8");
        logger.info(`PRD saved to: ${prdFile}`);

        const result = {
          success: true,
          message: "Code-to-PRD analysis completed",
          output: {
            file: prdFile,
            projectName,
            techStack: structure.techStack,
            frontendFileCount: structure.frontendFiles.length,
            backendFileCount: structure.backendFiles.length,
            configFiles: structure.configFiles,
          },
          nextSteps: [
            "Review the generated PRD",
            "AI should analyze frontend files to generate SolarWire wireframes",
            "AI should analyze backend routes to document API endpoints",
            "Update [To be confirmed] sections with actual data",
          ],
        };

        return {
          content: [
            {
              type: "text",
              text: JSON.stringify(result, null, 2),
            },
          ],
        };
      } catch (error) {
        const errorMessage = error instanceof Error ? error.message : String(error);
        logger.error(`Code-to-PRD failed: ${errorMessage}`);
        return {
          content: [
            {
              type: "text",
              text: `Error: ${errorMessage}`,
            },
          ],
          isError: true,
        };
      }
    }
  );
}
