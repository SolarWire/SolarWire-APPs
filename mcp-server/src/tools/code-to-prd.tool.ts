import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import fs from "fs";
import path from "path";
import { logger } from "../middleware/logger.js";

interface ProjectStructure {
  frontendFiles: string[];
  backendFiles: string[];
  configFiles: string[];
  techStack: string[];
}

function scanProject(basePath: string, frontendPaths: string[], backendPaths: string[]): ProjectStructure {
  const result: ProjectStructure = {
    frontendFiles: [],
    backendFiles: [],
    configFiles: [],
    techStack: [],
  };

  const scanDir = (dirPath: string, patterns: string[], target: "frontend" | "backend" | "config") => {
    if (!fs.existsSync(dirPath)) return;

    const walk = (currentPath: string) => {
      const entries = fs.readdirSync(currentPath, { withFileTypes: true });
      for (const entry of entries) {
        const fullPath = path.join(currentPath, entry.name);
        if (entry.name === "node_modules" || entry.name === ".git") continue;

        if (entry.isDirectory()) {
          walk(fullPath);
        } else {
          const relativePath = path.relative(basePath, fullPath);
          if (target === "frontend" && patterns.some((p) => relativePath.includes(p))) {
            result.frontendFiles.push(relativePath);
          } else if (target === "backend" && patterns.some((p) => relativePath.includes(p))) {
            result.backendFiles.push(relativePath);
          } else if (target === "config") {
            result.configFiles.push(relativePath);
          }
        }
      }
    };

    walk(dirPath);
  };

  scanDir(path.join(basePath, frontendPaths[0] || "src"), [".tsx", ".jsx", ".vue", ".html"], "frontend");
  scanDir(path.join(basePath, backendPaths[0] || "api"), [".ts", ".js", ".py", ".go"], "backend");

  // Detect tech stack
  const pkgPath = path.join(basePath, "package.json");
  if (fs.existsSync(pkgPath)) {
    const pkg = JSON.parse(fs.readFileSync(pkgPath, "utf-8"));
    const deps = { ...pkg.dependencies, ...pkg.devDependencies };
    if (deps.react || deps["@types/react"]) result.techStack.push("React");
    if (deps.vue) result.techStack.push("Vue");
    if (deps.express) result.techStack.push("Express");
    if (deps.nestjs || deps["@nestjs/core"]) result.techStack.push("NestJS");
    if (deps.next) result.techStack.push("Next.js");
    if (deps["tailwindcss"]) result.techStack.push("Tailwind CSS");
  }

  return result;
}

function extractUserStories(structure: ProjectStructure): string {
  const stories = [];
  if (structure.frontendFiles.length > 0) {
    stories.push("US-001 | As a user, I want to interact with the UI | Given context, when action, then result | P0");
  }
  if (structure.backendFiles.length > 0) {
    stories.push("US-002 | As a system, I need to process API requests | Given request, when processed, then response | P0");
  }
  return stories.map((s) => `| ${s} |`).join("\n");
}

